package model;

public class Library {
    private int numOfLoans;
    private int damageFine;
    private int lateFine;
    private int maxFine;

    public int getNumOfLoans() {
        return numOfLoans;
    }

    public void setNumOfLoans(int numOfLoans) {
        this.numOfLoans = numOfLoans;
    }

    public int getDamageFine() {
        return damageFine;
    }

    public void setDamageFine(int damageFine) {
        this.damageFine = damageFine;
    }

    public int getLateFine() {
        return lateFine;
    }

    public void setLateFine(int lateFine) {
        this.lateFine = lateFine;
    }

    public int getMaxFine() {
        return maxFine;
    }

    public void setMaxFine(int maxFine) {
        this.maxFine = maxFine;
    }
    
    
}
